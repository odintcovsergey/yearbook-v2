function isoDaysAgo(days: number): string {
  return new Date(Date.now() - days * 24 * 60 * 60 * 1000).toISOString()
}

async function main() {
  console.log(`\nРежим: ${APPLY ? '⚠️  APPLY (пишу в БД)' : 'СУХОЙ ПРОГОН (ничего не пишу)'}`)

  // 1) Автор — суперадмин.
  const { data: admin, error: adminErr } = await supabaseAdmin
    .from('users')
    .select('id, email, full_name')
    .eq('role', 'superadmin')
    .order('created_at', { ascending: true })
    .limit(1)
    .maybeSingle()
  if (adminErr) { console.error('✗ Ошибка поиска суперадмина:', adminErr.message); process.exit(1) }
  if (!admin) { console.error('✗ Не найден пользователь с role=superadmin. Создайте суперадмина и повторите.'); process.exit(1) }
  console.log(`Автор (скрыт в публичном API): ${admin.full_name ?? admin.email ?? admin.id}`)

  // 2) Что уже есть — для идемпотентности (сопоставляем по тексту body).
  const { data: existing, error: exErr } = await supabaseAdmin
    .from('ideas')
    .select('body')
    .limit(2000)
  if (exErr) { console.error('✗ Ошибка чтения ideas:', exErr.message); process.exit(1) }
  const seenBodies = new Set((existing ?? []).map((r: { body: string }) => r.body.trim()))
  console.log(`В таблице уже ${seenBodies.size} идей — их пропустим.`)

  // 3) Готовим строки на вставку.
  type Row = {
    body: string
    author_user_id: string
    status: 'done' | 'published'
    created_at: string
    published_at: string | null
    done_at: string | null
  }
  const rows: Row[] = []

  for (const s of DONE) {
    if (seenBodies.has(s.body.trim())) continue
    const doneAt = isoDaysAgo(s.daysAgo)
    rows.push({
      body: s.body,
      author_user_id: admin.id,
      status: 'done',
      // Запросили заметно раньше, потом опубликовали, потом сделали — органично.
      created_at: isoDaysAgo(s.daysAgo + 40),
      published_at: isoDaysAgo(s.daysAgo + 30),
      done_at: doneAt,
    })
  }
  for (const s of VOTING) {
    if (seenBodies.has(s.body.trim())) continue
    const at = isoDaysAgo(s.daysAgo)
    rows.push({
      body: s.body,
      author_user_id: admin.id,
      status: 'published',
      created_at: isoDaysAgo(s.daysAgo + 2),
      published_at: at,
      done_at: null,
    })
  }

  const doneNew = rows.filter(r => r.status === 'done').length
  const votingNew = rows.filter(r => r.status === 'published').length
  console.log(`\nК вставке: «Сделали» ${doneNew} из ${DONE.length}, «Голосование» ${votingNew} из ${VOTING.length}.`)
  console.log('(Голоса не проставляются — votes_count останется 0; на вкладке «Сделали» счётчик в UI скрыт.)')

  if (rows.length === 0) {
    console.log('\nНовых идей нет — всё уже засеяно. Ничего не делаю.\n')
    return
  }

  if (!APPLY) {
    console.log('\nЭто сухой прогон. Для реальной вставки запустите с флагом --apply.\n')
    return
  }

  const { error: insErr } = await supabaseAdmin.from('ideas').insert(rows)
  if (insErr) { console.error('\n✗ Ошибка вставки:', insErr.message); process.exit(1) }
  console.log(`\n✓ Готово. Вставлено ${rows.length} идей. Повторный запуск дубли не создаст.\n`)
}

main().catch((e) => { console.error('\n✗ Скрипт упал:', e); process.exit(1) })
