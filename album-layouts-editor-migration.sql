--
-- Назначение:
--
-- 1. `edited_at` — null если layout ни разу не редактировался руками
--    (значит можно безопасно пересобрать через buildAlbum). После
--    первой ручной правки в редакторе — заполнен. Используется
--    в модальном предупреждении «Макет редактировался вручную M дней
--    назад, все правки будут потеряны» при попытке пересборки.
--
-- 2. `edited_by` — кто последний раз правил. Для audit и UI
--    «правил Иван Иванов 5 мин назад».
--
-- 3. `rebuild_warnings` — последние предупреждения от buildAlbum
--    (массив объектов code+detail). Если партнёр захочет пересобрать
--    заново после правок — увидит что было.
--
-- Все три поля nullable/default, миграция аддитивная — НИ ОДНА
-- существующая запись не сломается. Существующие layout'ы получат
-- NULL во всех трёх (что эквивалентно «никогда не редактировались»).
-- ════════════════════════════════════════════════════════════════════

ALTER TABLE album_layouts
  ADD COLUMN IF NOT EXISTS edited_at timestamptz,
  ADD COLUMN IF NOT EXISTS edited_by uuid REFERENCES users(id) ON DELETE SET NULL,
  ADD COLUMN IF NOT EXISTS rebuild_warnings jsonb DEFAULT '[]'::jsonb;

-- Индекс для быстрого поиска недавно отредактированных layout'ов
-- (для будущей вкладки «Последние правки» и т.п.). Композитный
-- по (album_id, edited_at) — основные запросы будут «когда правился
-- layout этого альбома».
CREATE INDEX IF NOT EXISTS idx_album_layouts_edited_at
  ON album_layouts(album_id, edited_at DESC);

