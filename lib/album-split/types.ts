/**
 * Модель нарезки собранного альбома на per-student книги (ТЗ 19.06.2026
 * «персональный раздел»).
 *
 * Здесь — только МОДЕЛЬ: как разворот относится к книге, как считается номер
 * книги/разворота, как имя файла. Сам рендер в JPG/PDF и выгрузка — отдельное
 * ТЗ (экспорт). Вход — развороты из AlbumLayout (SpreadInstance с left/right),
 * на личных страницах стоит метка PageInstance.personal (её ставит движок).
 *
 * Нумерация имён файлов: «КНИГА-РАЗВОРОТ» с ведущими нулями.
 *  - Общая книга:    000 → 000-00 (обложка), 000-01, 000-02 …
 *  - Личная книга:   00X → 00X-00 (обложка), 00X-01, 00X-02 …  (X = номер ученика)
 * Позиция разворота нумеруется внутри книги с 01; обложка = 00.
 */

import type { PageInstance } from '../rule-engine/types';

export type BookKind = 'common' | 'personal';

/** Один разворот внутри книги (после регруппировки страниц по книгам). */
export interface BookSpread {
  /** Позиция разворота внутри книги, 1-based (YY в имени файла). */
  position: number;
  /** Имя файла «КНИГА-РАЗВОРОТ», напр. "001-03". */
  file_name: string;
  left?: PageInstance;
  right?: PageInstance;
  /** true если обе страницы пришли из одного is_spread-разворота источника. */
  is_spread?: boolean;
  /** spread_index источника (для трассировки/отладки). */
  source_spread_index?: number;
}

export interface Book {
  /** "000" — общая книга; "001"/"002"… — личная книга ученика. */
  book_id: string;
  kind: BookKind;
  /** Индекс ученика (из метки personal); только для kind='personal'. */
  student_index?: number;
  /** Имя файла обложки книги: "000-00" / "001-00". */
  cover_file_name: string;
  /** Развороты книги по порядку (без обложки). */
  spreads: BookSpread[];
}

export interface AlbumBooks {
  /** Книга 000 (общая) первой, далее личные книги по возрастанию номера. */
  books: Book[];
  /** Есть ли хотя бы одна личная книга (т.е. была персональная секция). */
  has_personal: boolean;
}
