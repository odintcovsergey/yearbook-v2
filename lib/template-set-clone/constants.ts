/**
 * РЭ.28: константы для resize'а partner-клонов template_set'ов.
 *
 * Все mm-значения после resize'а кратны MM_STEP — это гарантирует
 * что в типографском растре при 300 DPI они попадут в целые пиксели.
 */

/**
 * Типографское разрешение в DPI.
 *
 * Стандарт коммерческой полиграфии — 300 DPI. Если в будущем партнёру
 * понадобится другое (240 эконом / 600 премиум) — параметр пробросится
 * из API. Сейчас — хардкод (см. РЭ.28-spec §3.1, развилка H).
 */
export const PRINT_DPI = 300;

/**
 * Размер одного пикселя в миллиметрах при текущем DPI.
 *
 * 1 inch = 25.4 mm, при 300 DPI:
 *   25.4 / 300 ≈ 0.0846666... mm
 *
 * Все resized mm-значения округляются до кратности этой константе.
 */
export const MM_STEP = 25.4 / PRINT_DPI;
