/**
 * РЭ.28: округление mm-значений до целых пикселей при 300 DPI.
 *
 * После resize'а partner-клона все mm-значения (размеры страниц,
 * spread'ов, placeholder'ов) проходят через roundMmToPx — это
 * гарантирует что в типографском растре каждый размер попадёт в
 * ровную клетку пикселей. Иначе печать с округлением даст
 * дробные значения, что для некоторых типографий критично
 * (Сергей подтвердил 21.05.2026).
 *
 * Чистая функция, без зависимостей от Supabase.
 */

import { MM_STEP } from './constants';

/**
 * Округляет mm-значение до ближайшего целого пикселя при 300 DPI.
 *
 * Значение остаётся в миллиметрах, но становится кратно MM_STEP
 * (~0.0847 мм). Это даёт точные размеры в пикселях для типографии
 * без дробных частей.
 *
 * Идемпотентно: roundMmToPx(roundMmToPx(x)) === roundMmToPx(x).
 *
 * Примеры (при PRINT_DPI=300, MM_STEP ≈ 0.0847):
 *   roundMmToPx(0)         → 0
 *   roundMmToPx(210)       → 210.058... (2480 px ровно)
 *   roundMmToPx(91.21031)  → 91.1947... (1077 px ровно)
 */
export function roundMmToPx(mm: number): number {
  if (!Number.isFinite(mm)) return mm;
  const px = Math.round(mm / MM_STEP);
  return px * MM_STEP;
}

/**
 * Конверсия mm → целые пиксели при 300 DPI.
 *
 * Используется в UI для информационной подсказки рядом с полями
 * ввода размеров: «210 мм ≈ 2480 px». Партнёр не редактирует px
 * напрямую (хардкод DPI = 300), но видит финальное значение.
 *
 * Не симметрично roundMmToPx — здесь возвращаем целое число px,
 * а не mm-значение.
 */
export function mmToPx(mm: number): number {
  if (!Number.isFinite(mm)) return 0;
  return Math.round(mm / MM_STEP);
}
