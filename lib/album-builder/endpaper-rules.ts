/**
 * РЭ.27.3: endpaper-rules — описание правил автозаглушек «Форзац»
 * для soft-альбомов.
 *
 * Зачем:
 * В мягком переплёте есть полиграфические требования:
 * - Первая страница после обложки должна быть ПРАВОЙ (нечётная,
 *   титульная). Перед ней слева — форзац.
 * - Последняя страница должна быть ЛЕВОЙ (чётная). После неё справа —
 *   форзац.
 *
 * Для layflat правила не применяются (см. spec §3.3).
 *
 * Архитектурное решение в РЭ.27.3:
 * Эта функция — ПЛАН правил, а не их применение. Она возвращает
 * описание «куда должны быть вставлены форзацы», но НЕ модифицирует
 * layout. Применение и отрисовка водяного знака — в подэтапе 27.4
 * (одновременно с правкой render-preview-svg).
 *
 * Это даёт безопасность 27.3: мы не трогаем существующие 407 тестов
 * и прод-сборку. Когда придёт 27.4 — отрисовка получит готовые правила
 * через эту функцию.
 *
 * Чистая функция, тестируется без Supabase.
 */

import type { PrintType } from '@/lib/album-builder/types';

/**
 * Где конкретно должна стоять автозаглушка «Форзац».
 *
 * first_left — левая страница первого разворота альбома
 * last_right — правая страница последнего разворота альбома
 */
export type EndpaperPosition = 'first_left' | 'last_right';

/**
 * Описание одной автозаглушки.
 *
 * Будет использовано в 27.4 для:
 * - вставки EndpaperInstance в layout перед отрисовкой,
 * - отрисовки водяного знака «Форзац» бледным курсивом по центру.
 */
export type EndpaperSpec = {
  position: EndpaperPosition;
  /** Метка для отображения (фронт может локализовать). */
  label: string;
};

/**
 * Возвращает список правил автозаглушек для альбома по типу переплёта.
 *
 * - layflat → пустой список (правила не применяются).
 * - soft → две заглушки: forward (первый разворот, левая) и
 *   backward (последний разворот, правая).
 *
 * Функция чистая, не зависит от текущего layout. Просто описывает что
 * должно быть. Caller (или подэтап 27.4) сам решает как применить.
 */
export function getEndpaperRules(printType: PrintType): EndpaperSpec[] {
  if (printType !== 'soft') return [];
  return [
    { position: 'first_left', label: 'Форзац' },
    { position: 'last_right', label: 'Форзац' },
  ];
}
