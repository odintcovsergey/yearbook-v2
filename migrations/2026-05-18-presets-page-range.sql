-- РЭ.21.5.1 — диапазон страниц вместо фиксированного total_pages.
--
-- Контекст: total_pages у пресета — это конкретное число (например, 24).
-- На практике для каждой комплектации существует диапазон допустимых
-- значений (Мини: 6-6 фиксированный; Стандарт: 20-50 в зависимости от
-- количества учеников). Партнёр настраивает диапазон в пресете один раз,
-- фактическое число выбирается на уровне альбома.
--
-- Стратегия:
--   1. ADD COLUMN min_pages, max_pages (nullable, без дефолта).
--   2. total_pages пока ОСТАЁТСЯ (NOT NULL, default 24) — это фолбэк
--      для кода: если min/max не заполнены, читаем total_pages как
--      min=max=total_pages.
--   3. Дефолтные значения для 7 встроенных пресетов проставит Сергей
--      вручную через SQL или UI после применения миграции.
--   4. Колонку total_pages удалим в РЭ.21.5.3, когда убедимся что весь
--      код ходит через min/max и нет регрессий на проде.

ALTER TABLE presets ADD COLUMN min_pages integer;
ALTER TABLE presets ADD COLUMN max_pages integer;

