-- Фотопапка-тримо (отдельный продукт): тип продукта у дизайна.
--
-- До сих пор ЛЮБОЙ template_set по умолчанию считался альбомом — отдельного
-- поля типа не было (был только print_type = переплёт layflat/soft, это НЕ
-- тип продукта). Чтобы система отличала «это фотопапка» от «это альбом» на
-- уровне выбора дизайна, добавляем product_type.
--
-- Значения:
--   'album'       — школьный альбом (текущий и единственный продукт)
--   'photofolder' — фотопапка-тримо (3 панели; позже двойная — 2 панели)
--
-- ВАЖНО:
--   - default 'album' — все СУЩЕСТВУЮЩИЕ и новые дизайны остаются альбомами.
--     Старый код, который не знает про фотопапку, продолжает работать как
--     раньше (фильтр по типу просто не отсечёт ни одного текущего дизайна).
--   - CHECK ограничивает набор значений, чтобы опечатка не создала «третий
--     тип» молча. Новый тип продукта = расширить CHECK отдельной миграцией.
--
-- Применять ДО деплоя кода, который читает product_type (фильтр дизайнов по
-- типу продукта в форме заказа).

alter table template_sets
  add column if not exists product_type text not null default 'album'
    check (product_type in ('album', 'photofolder'));
