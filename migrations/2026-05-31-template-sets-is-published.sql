-- Блок A (управление дизайнами): поле публикации дизайна.
--
-- is_published отделяет «черновик» от «опубликованного» НЕЗАВИСИМО от
-- глобальности (tenant_id). Загрузил дизайн, дорабатываешь — партнёры его
-- не видят пока is_published = false.
--
-- ВАЖНО:
--   - default FALSE — НОВЫЕ дизайны создаются черновиками.
--   - НО все СУЩЕСТВУЮЩИЕ дизайны проставляем TRUE, иначе «Белый» и
--     «Акварельные мечты» пропадут у партнёров сразу после деплоя фильтра.
--
-- Применять ДО деплоя кода с фильтром is_published в партнёрской выдаче.

alter table template_sets
  add column if not exists is_published boolean not null default false;

-- Все существующие дизайны делаем опубликованными (разовый UPDATE).
update template_sets
  set is_published = true;
