-- РЭ.40.a — student_distribution: умное распределение учеников по сетке.
--
-- Контекст:
--   Сергей выявил что жадное распределение (12 + 12 + 6 для 30 учеников
--   при сетке 12) визуально неравномерно: хвост 6 рисуется в другом
--   мастере с более крупными слотами, выглядит как «выделение». Хочется
--   адаптивную логику: 10 + 10 + 10 или (для случаев когда есть общее
--   фото) 10 + 10 + (3 + общее фото) — combined-мастер.
--
-- Решение:
--   Новое поле albums.student_distribution с тремя режимами:
--     • 'greedy' — жадное распределение, текущее поведение
--       (12+12+6, может срабатывать симметризация для хвоста 1)
--     • 'equalize' — всегда равномерно (10+10+10), даже если есть фото
--     • 'auto' — умный алгоритм:
--       1) если есть свободное общее фото И combined-мастер вмещает
--          подходящее количество → equalize + combined-tail (10+combined-3)
--       2) иначе чистый equalize (10+10+10)
--
-- Уровень настройки — альбом (а не пресет), потому что Сергею удобнее
-- переключать на лету для разных классов без редактирования шаблона.
--
-- Совместимость:
--   Применяется ТОЛЬКО к density-режимам 'mini' и 'light' (где есть
--   grid-сетка с одним мастером на полную страницу). Для 'medium',
--   'standard', 'universal' поле игнорируется (там другая модель —
--   alternating L/R).
--
-- DEFAULT 'auto':
--   Все существующие альбомы получают новую логику сразу. Сергей
--   подтвердил это решение — он готов к тому что для некоторых старых
--   альбомов layout перестроится. Если кто-то хочет вернуть прежний
--   вид — переключит на 'greedy' в карточке альбома.

ALTER TABLE albums
  ADD COLUMN IF NOT EXISTS student_distribution text DEFAULT 'auto'
  CHECK (student_distribution IN ('auto', 'equalize', 'greedy'));

