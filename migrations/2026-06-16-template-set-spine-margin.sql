-- Модель «поля» (16.06.2026): отступ контента от корешка на уровне дизайна.
--
-- Зачем: дизайнерские наборы (напр. «Аква меч») рисуют контент с произвольным
-- сдвигом, а авто-зеркало честно его отражает → контент жмётся к корешку на
-- обеих страницах. Новая модель: система ставит блок контента так, чтобы у
-- КОРЕШКА было ровно spine_margin_mm мм (внешний край получает остаток).
-- Заменяет авто-зеркало для дизайнов, где поле задано.
--
-- NULL (дефолт) = прежнее поведение (авто-зеркало page-any). Аддитивно,
-- обратносовместимо: существующие наборы (okeybook «Белый») не затронуты.
-- Это render-time настройка — перезаливка мастеров НЕ нужна, достаточно
-- пересобрать/обновить альбом.

ALTER TABLE template_sets
  ADD COLUMN IF NOT EXISTS spine_margin_mm numeric NULL;

COMMENT ON COLUMN template_sets.spine_margin_mm IS
  'Модель «поля»: отступ контента от корешка (мм). NULL = legacy авто-зеркало page-any. Задан = система ставит блок с этим полем у корешка на каждой стороне (заменяет зеркало). Один на весь набор.';

