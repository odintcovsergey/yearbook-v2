-- РЭ.37.1 — Конструктор переходного раздела, флаг симметризации хвоста
-- students-секции.
--
-- Контекст:
-- Когда в шаблоне students-секция использует сетку портретов (N-Grid-6
-- или N-Grid-12) и количество учеников такое, что в последнем ряду остался
-- один портрет (хвост = 1) — визуально это некрасиво (одинокий портрет
-- с краю). Решение: если флаг включён, build engine при сборке альбома
-- забирает один портрет с предыдущей полной страницы; теперь на хвостовой
-- странице 2 портрета, а на предыдущей — на 1 меньше (неполный последний
-- ряд). Оба «дефицита» центрируются через placeholder_centering.
--
-- Поле работает только для комплектаций Мини/Лайт (определяется наличием
-- N-Grid-12 / N-Grid-6 в template_set). Для Медиум/Стандарт/Универсал/
-- Максимум флаг игнорируется engine'ом (UI его скрывает/disabled — см.
-- план РЭ.37.5).
--
-- has_user_edits=true на уровне альбома уважается: если партнёр вручную
-- правил последний разворот students — engine не перебалансирует.
--
-- Совместимость: NOT NULL DEFAULT FALSE — все существующие пресеты
-- получают значение false (опт-ин). Старый код, не знающий про колонку,
-- не ломается: SELECT * вернёт лишнюю колонку (игнорируется), INSERT
-- без указания поля использует DEFAULT.
--
-- См. также:
--   docs/transition-section-spec.md §3.4, §5.2
--   yearbook-context-v155.md (фиксация решения, 24.05.2026 вечер)

ALTER TABLE presets
  ADD COLUMN IF NOT EXISTS symmetrize_students_tail BOOLEAN NOT NULL DEFAULT FALSE;

